	public static String getOfficeType(String id){
		Office office = officeDao.get(id);
		if (office != null){
			return DictUtils.getDictLabel(office.getFtype(), "ftype", "");
		}
		return "";
	}
	public static String getOfficeName(String id){
		Office office = officeDao.get(id);
		if (office != null){
			return office.getName();
		}
		return "";
	}
	public static Office getOffice(String id){
		Office office = officeDao.get(id);
		if (office != null){
			return office;
		}
		return null;
	}
	
